<template>
  <div class="header-wrapper">
    <div class="left-wrapper">
      <h1 class="logo">得到</h1>
      <div v-for="(item, index) in titleList" :key="index" class="item">
        <nuxt-link :to="item.route">{{ item.name }}</nuxt-link>
      </div>
    </div>
    <div class="right-wrapper">
      <el-input
        class="item"
        size="small"
        v-model="input"
        suffix-icon="el-icon-search"
        placeholder="探索得到"
      ></el-input>
      <el-button
        class="item"
        @click="openPage('/writing')"
        type="primary"
        size="small"
        >写文章</el-button
      >
      <el-badge :value="3" class="item">
        <i class="el-icon-bell"></i>
      </el-badge>
      <div @click.stop="login">
        <el-avatar
          class="avatar item"
          :src="userInfo ? userInfo.avatar : ''"
        ></el-avatar>
      </div>
    </div>
    <!-- 用户面板 -->
    <user-info v-if="isShowInfo" />
  </div>
</template>

<script>
import { getLocalStorage } from "@/utils/store";
import userInfo from "./userInfo.vue";

export default {
  components: { userInfo },
  data() {
    return {
      userInfo: null, // 用户信息
      input: "",
      circleUrl: "",
      titleList: [
        {
          name: "首页",
          route: "/"
        },
        {
          name: "互动",
          route: "/circle"
        },
        {
          name: "资讯",
          route: "/news"
        }
      ]
    };
  },
  computed: {
    isLogin() {
      return this.$store.state.login.isLogin;
    },
    isShowInfo() {
      return this.$store.state.login.isShowInfo;
    }
  },
  watch: {
    isLogin() {
      this.getUserInfo();
    }
  },
  mounted() {
    // 获取用户信息：
    this.getUserInfo();
  },
  methods: {
    getUserInfo() {
      const userInfo = getLocalStorage("user_info");

      this.userInfo = userInfo;
    },
    openPage(path) {
      window.open(path, "_blank");
    },
    login() {
      const isLogin = getLocalStorage("user_token") ? true : false;

      if (!isLogin) {
        return this.$store.commit("login/toggleOpen", true);
      }

      this.$store.commit("login/toggleInfoOpen", true);
    }
  }
};
</script>

